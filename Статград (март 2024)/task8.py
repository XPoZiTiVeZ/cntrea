def r(n):
    if len(n) == 16:
        return 1
    
    if int(n[-1]) % 2 == 0:
        return r(n + "1") + r(n + "3") + r(n + "5")
    
    if int(n[-1]) == 3:
        return r(n + "0") + r(n + "2")
    
    if int(n[-1]) in (1, 5):
        return r(n + "0") + r(n + "2") + r(n + "4")
    
    return r(n + "0") + r(n + "2") + r(n + "4") + r(n + "6")

b1 = r("1")
b2 = r("2")
b3 = r("3")
b4 = r("4")
b5 = r("5")
b6 = r("6")

print(b1 + b2 + b3 + b4 + b5 + b6)

